package application;

public class BooksUsers {
	String user , book;

	public BooksUsers(String user, String book) {
		super();
		this.user = user;
		this.book = book;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getBook() {
		return book;
	}

	public void setBook(String book) {
		this.book = book;
	}
	
}
